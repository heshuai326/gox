package gox

type Pair struct {
	First  interface{}
	Second interface{}
}

type Int64Pair struct {
	First  int64
	Second int64
}

type IntPair struct {
	First  int
	Second int
}

type Float64Pair struct {
	First  float64
	Second float64
}

type Float32Pair struct {
	First  float32
	Second float32
}

type StringPair struct {
	First  string
	Second string
}
